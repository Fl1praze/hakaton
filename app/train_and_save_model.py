# –î–ª—è —Ä–∞–±–æ—Ç—ã —Å –ø—É—Ç—è–º–∏ (—Ñ–∞–π–ª—ã/–ø–∞–ø–∫–∏)
from pathlib import Path

# –î–ª—è —Ä–∞–±–æ—Ç—ã —Å JSON —Ñ–∞–π–ª–∞–º–∏
import json

# –§–£–ù–ö–¶–ò–Ø –ó–ê–ì–†–£–ó–ö–ò –û–ë–£–ß–ê–Æ–©–ò–• –î–ê–ù–ù–´–•
def load_training_data(json_path="training_data.json"):
    """
    –ß—Ç–æ –¥–µ–ª–∞–µ—Ç —Ñ—É–Ω–∫—Ü–∏—è:
        1. –û—Ç–∫—Ä—ã–≤–∞–µ—Ç JSON —Ñ–∞–π–ª
        2. –ß–∏—Ç–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –ø—Ä–∏–º–µ—Ä–æ–≤
        3. –ö–∞–∂–¥—ã–π –ø—Ä–∏–º–µ—Ä –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ—Ç –≤ –∫–æ—Ä—Ç–µ–∂ (—Ç–µ–∫—Å—Ç, —Å–ª–æ–≤–∞—Ä—å —Å –¥–∞–Ω–Ω—ã–º–∏)
        4. –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π
    """
    # –ü–µ—á–∞—Ç–∞–µ–º –∑–∞–≥–æ–ª–æ–≤–æ–∫
    print("\n" + "="*70)
    print("–ó–ê–ì–†–£–ó–ö–ê –û–ë–£–ß–ê–Æ–©–ò–• –î–ê–ù–ù–´–•")
    print("="*70)
    
    try:
        with open(json_path, 'r', encoding='utf-8') as f:
            raw_data = json.load(f)
        print(f"‚úÖ –ó–∞–≥—Ä—É–∂–µ–Ω–æ –ø—Ä–∏–º–µ—Ä–æ–≤ –∏–∑ —Ñ–∞–π–ª–∞: {len(raw_data)}")

        # –ë—ã–ª–æ: [{"text": "...", "data": {...}}, ...]
        # –°—Ç–∞–Ω–µ—Ç: [("—Ç–µ–∫—Å—Ç", {...}), ...]
        training_data = []
        
        for item in raw_data:
            # item = {"text": "–ê–û –¢–ê–ù–î–ï–† –ò–ù–ù 2310031475 ...", "data": {"inn": "2310031475", ...}}
            # –ò–∑–≤–ª–µ–∫–∞–µ–º —Ç–µ–∫—Å—Ç —á–µ–∫–∞
            text = item['text']
            # –ò–∑–≤–ª–µ–∫–∞–µ–º –¥–∞–Ω–Ω—ã–µ (–ò–ù–ù, –Ω–∞–∑–≤–∞–Ω–∏–µ, –¥–∞—Ç–∞, —Å—É–º–º–∞)
            data = item['data']
            # –î–æ–±–∞–≤–ª—è–µ–º –≤ —Å–ø–∏—Å–æ–∫ –∫–∞–∫ –∫–æ—Ä—Ç–µ–∂ (—Ç–µ–∫—Å—Ç, –¥–∞–Ω–Ω—ã–µ)
            training_data.append((text, data))
        
        # –í—ã–≤–æ–¥–∏–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
        print(f"‚úÖ –ü–æ–¥–≥–æ—Ç–æ–≤–ª–µ–Ω–æ –ø—Ä–∏–º–µ—Ä–æ–≤ –¥–ª—è –æ–±—É—á–µ–Ω–∏—è: {len(training_data)}")
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–µ—Ä–≤—ã–π –ø—Ä–∏–º–µ—Ä (–¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏)
        if training_data:
            print("\nüìã –ü—Ä–∏–º–µ—Ä –æ–±—É—á–∞—é—â–∏—Ö –¥–∞–Ω–Ω—ã—Ö:")
            print(f"   –¢–µ–∫—Å—Ç: {training_data[0][0][:100]}...")  # –ü–µ—Ä–≤—ã–µ 100 —Å–∏–º–≤–æ–ª–æ–≤
            print(f"   –î–∞–Ω–Ω—ã–µ: {training_data[0][1]}")
        return training_data
        
    except FileNotFoundError:
        # –ï—Å–ª–∏ —Ñ–∞–π–ª –Ω–µ –Ω–∞–π–¥–µ–Ω
        print(f"‚ùå –û–®–ò–ë–ö–ê: –§–∞–π–ª {json_path} –Ω–µ –Ω–∞–π–¥–µ–Ω!")
        print("   –ó–∞–ø—É—Å—Ç–∏—Ç–µ —Å–Ω–∞—á–∞–ª–∞: python auto_extract_training_data.py")
        return []
    
    except json.JSONDecodeError as e:
        # –ï—Å–ª–∏ —Ñ–∞–π–ª –ø–æ–≤—Ä–µ–∂–¥–µ–Ω –∏–ª–∏ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π JSON
        print(f"‚ùå –û–®–ò–ë–ö–ê: –ù–µ —É–¥–∞–ª–æ—Å—å –ø—Ä–æ—á–∏—Ç–∞—Ç—å JSON —Ñ–∞–π–ª: {e}")
        return []


# –ì–õ–ê–í–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø –û–ë–£–ß–ï–ù–ò–Ø
def main():
    """
    –ì–ª–∞–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∫–æ—Ç–æ—Ä–∞—è –∑–∞–ø—É—Å–∫–∞–µ—Ç –≤–µ—Å—å –ø—Ä–æ—Ü–µ—Å—Å –æ–±—É—á–µ–Ω–∏—è
    
    –®–∞–≥–∏:
        1. –ó–∞–≥—Ä—É–∂–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –∏–∑ training_data.json
        2. –°–æ–∑–¥–∞–µ—Ç –º–æ–¥–µ–ª—å
        3. –û–±—É—á–∞–µ—Ç –º–æ–¥–µ–ª—å (30 —ç–ø–æ—Ö)
        4. –°–æ—Ö—Ä–∞–Ω—è–µ—Ç –æ–±—É—á–µ–Ω–Ω—É—é –º–æ–¥–µ–ª—å
        5. –¢–µ—Å—Ç–∏—Ä—É–µ—Ç –Ω–∞ –Ω–æ–≤—ã—Ö –¥–∞–Ω–Ω—ã—Ö
        6. –í—ã–≤–æ–¥–∏—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –º–æ–¥–µ–ª–∏
    """
    # –®–ê–ì 1: –ó–ê–ì–†–£–ó–ö–ê –î–ê–ù–ù–´–•
    # –í—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –∑–∞–≥—Ä—É–∑–∫–∏ –¥–∞–Ω–Ω—ã—Ö
    training_data = load_training_data("training_data.json")
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º —á—Ç–æ –¥–∞–Ω–Ω—ã–µ –∑–∞–≥—Ä—É–∂–µ–Ω—ã
    if not training_data:
        print("\n‚ùå –ù–µ—Ç –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –æ–±—É—á–µ–Ω–∏—è. –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ —Ä–∞–±–æ—Ç—ã.")
        return

    # –®–ê–ì 2: –°–û–ó–î–ê–ù–ò–ï –ú–û–î–ï–õ–ò
    print("\n" + "="*70)
    print("–°–û–ó–î–ê–ù–ò–ï –ú–û–î–ï–õ–ò")
    print("="*70)
    
    # –°–æ–∑–¥–∞–µ–º —ç–∫–∑–µ–º–ø–ª—è—Ä –∫–ª–∞—Å—Å–∞ InvoiceDataExtractor
    # –ü—Ä–∏ —Å–æ–∑–¥–∞–Ω–∏–∏ –º–æ–¥–µ–ª—å –∏–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ—Ç:
    # - regex –ø–∞—Ç—Ç–µ—Ä–Ω—ã –¥–ª—è –ø–æ–∏—Å–∫–∞ (–ò–ù–ù, –Ω–∞–∑–≤–∞–Ω–∏–µ, –¥–∞—Ç–∞, —Å—É–º–º–∞)
    # - –≤–µ—Å–∞ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ –ø–∞—Ç—Ç–µ—Ä–Ω–∞ (–Ω–∞—á–∞–ª—å–Ω—ã–µ = 1.0)
    model = InvoiceDataExtractor()
    
    print("‚úÖ –ú–æ–¥–µ–ª—å InvoiceDataExtractor —Å–æ–∑–¥–∞–Ω–∞")
    print(f"–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø–æ–ª–µ–π –¥–ª—è –∏–∑–≤–ª–µ—á–µ–Ω–∏—è: 4 (inn, vendor, date, total)")

    # –®–ê–ì 3: –û–ë–£–ß–ï–ù–ò–ï –ú–û–î–ï–õ–ò
    print("\n" + "="*70)
    print("–û–ë–£–ß–ï–ù–ò–ï –ú–û–î–ï–õ–ò")
    print("="*70)
    print(f"–†–∞–∑–º–µ—Ä –¥–∞—Ç–∞—Å–µ—Ç–∞: {len(training_data)} –ø—Ä–∏–º–µ—Ä–æ–≤")
    print(f"–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ø–æ—Ö: 30")
    print("="*70 + "\n")
    
    # –í—ã–∑—ã–≤–∞–µ–º –º–µ—Ç–æ–¥ train() –º–æ–¥–µ–ª–∏
    # –ü–∞—Ä–∞–º–µ—Ç—Ä—ã:
    #   - training_data: —Å–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π (—Ç–µ–∫—Å—Ç, –¥–∞–Ω–Ω—ã–µ)
    #   - epochs: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ø–æ—Ö –æ–±—É—á–µ–Ω–∏—è (–ø—Ä–æ—Ö–æ–¥–æ–≤ –ø–æ –≤—Å–µ–º –¥–∞–Ω–Ω—ã–º)
    # 
    # –ß—Ç–æ –ø—Ä–æ–∏—Å—Ö–æ–¥–∏—Ç –≤–Ω—É—Ç—Ä–∏ train():
    #   –ù–∞ –∫–∞–∂–¥–æ–π —ç–ø–æ—Ö–µ:
    #     1. –ü—Ä–æ—Ö–æ–¥–∏—Ç –ø–æ –∫–∞–∂–¥–æ–º—É –ø—Ä–∏–º–µ—Ä—É
    #     2. –ò–∑–≤–ª–µ–∫–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ —Å —Ç–µ–∫—É—â–∏–º–∏ –≤–µ—Å–∞–º–∏
    #     3. –°—Ä–∞–≤–Ω–∏–≤–∞–µ—Ç —Å –ø—Ä–∞–≤–∏–ª—å–Ω—ã–º –æ—Ç–≤–µ—Ç–æ–º
    #     4. –û–±–Ω–æ–≤–ª—è–µ—Ç –≤–µ—Å–∞:
    #        - –ï—Å–ª–∏ –ø–∞—Ç—Ç–µ—Ä–Ω —Å—Ä–∞–±–æ—Ç–∞–ª –ø—Ä–∞–≤–∏–ª—å–Ω–æ ‚Üí –≤–µ—Å ‚Üë
    #        - –ï—Å–ª–∏ –ø–∞—Ç—Ç–µ—Ä–Ω —Å—Ä–∞–±–æ—Ç–∞–ª –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ ‚Üí –≤–µ—Å ‚Üì
    #     5. –°—á–∏—Ç–∞–µ—Ç –º–µ—Ç—Ä–∏–∫–∏ (loss, accuracy)
    #     6. –í—ã–≤–æ–¥–∏—Ç –ø—Ä–æ–≥—Ä–µ—Å—Å –∫–∞–∂–¥—ã–µ 5 —ç–ø–æ—Ö
    model.train(training_data, epochs=30)
    
    # –®–ê–ì 4: –°–û–•–†–ê–ù–ï–ù–ò–ï –ú–û–î–ï–õ–ò
    print("\n" + "="*70)
    print("–°–û–•–†–ê–ù–ï–ù–ò–ï –ú–û–î–ï–õ–ò")
    print("="*70)
    
    # –ü—É—Ç—å –∫—É–¥–∞ —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å –º–æ–¥–µ–ª—å
    model_path = "models/invoice_extractor.pkl"
    
    # –°–æ–∑–¥–∞–µ–º –ø–∞–ø–∫—É models/ –µ—Å–ª–∏ –µ–µ –Ω–µ—Ç
    Path("models").mkdir(exist_ok=True)
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –º–æ–¥–µ–ª—å –≤ —Ñ–∞–π–ª
    # –°–æ—Ö—Ä–∞–Ω—è–µ—Ç—Å—è:
    #   - –í—Å–µ regex –ø–∞—Ç—Ç–µ—Ä–Ω—ã
    #   - –û–±—É—á–µ–Ω–Ω—ã–µ –≤–µ—Å–∞
    #   - –ò—Å—Ç–æ—Ä–∏—è –æ–±—É—á–µ–Ω–∏—è (loss, accuracy –ø–æ —ç–ø–æ—Ö–∞–º)
    #   - –ú–µ—Ç–∞–¥–∞–Ω–Ω—ã–µ (–≤–µ—Ä—Å–∏—è, –¥–∞—Ç–∞ –æ–±—É—á–µ–Ω–∏—è)
    model.save(model_path)
    
    print(f"‚úÖ –ú–æ–¥–µ–ª—å —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∞: {model_path}")
    
    # –®–ê–ì 5: –¢–ï–°–¢–ò–†–û–í–ê–ù–ò–ï –ù–ê –ù–û–í–´–• –î–ê–ù–ù–´–•
    print("\n" + "="*70)
    print("–¢–ï–°–¢–ò–†–û–í–ê–ù–ò–ï –ù–ê –ù–û–í–´–• –î–ê–ù–ù–´–•")
    print("="*70)
    # –°–æ–∑–¥–∞–µ–º —Ç–µ—Å—Ç–æ–≤—ã–π —Ç–µ–∫—Å—Ç (–º–æ–¥–µ–ª—å –µ–≥–æ —Ä–∞–Ω—å—à–µ –Ω–µ –≤–∏–¥–µ–ª–∞)
    test_text = """
    –û–û–û "–¢–ï–°–¢–û–í–ê–Ø –ö–û–ú–ü–ê–ù–ò–Ø"
    –ò–ù–ù 1122334455
    –î–∞—Ç–∞: 20.11.2025
    –ö–∞—Å—Å–æ–≤—ã–π —á–µ–∫
    –ò–¢–û–ì–û: 777.77 —Ä—É–±
    """
    print("\nüìÑ –¢–µ—Å—Ç–æ–≤—ã–π —Ç–µ–∫—Å—Ç:")
    print(test_text)
    
    # –í—ã–∑—ã–≤–∞–µ–º –º–µ—Ç–æ–¥ predict() –¥–ª—è –∏–∑–≤–ª–µ—á–µ–Ω–∏—è –¥–∞–Ω–Ω—ã—Ö
    # –ú–æ–¥–µ–ª—å –ø—Ä–∏–º–µ–Ω—è–µ—Ç –æ–±—É—á–µ–Ω–Ω—ã–µ –≤–µ—Å–∞ –∫ regex –ø–∞—Ç—Ç–µ—Ä–Ω–∞–º
    result = model.predict(test_text)
    print("\nüìä –ò–∑–≤–ª–µ—á–µ–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ:")
    # –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ –∫—Ä–∞—Å–∏–≤–æ–º —Ñ–æ—Ä–º–∞—Ç–µ JSON
    print(json.dumps(result, ensure_ascii=False, indent=2))
    
    # –®–ê–ì 6: –ò–ù–§–û–†–ú–ê–¶–ò–Ø –û –ú–û–î–ï–õ–ò
    print("\n" + "="*70)
    print("–ò–ù–§–û–†–ú–ê–¶–ò–Ø –û –ú–û–î–ï–õ–ò")
    print("="*70)
    
    # –ü–æ–ª—É—á–∞–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –º–æ–¥–µ–ª–∏
    info = model.get_training_info()
    
    # –í—ã–≤–æ–¥–∏–º –º–µ—Ç—Ä–∏–∫–∏
    print(f"üì¶ –í–µ—Ä—Å–∏—è –º–æ–¥–µ–ª–∏: {info['model_version']}")
    print(f"üìÖ –î–∞—Ç–∞ –æ–±—É—á–µ–Ω–∏—è: {info['training_date']}")
    print(f"üîÑ –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ø–æ—Ö: {info['epochs']}")
    print(f"üéØ –§–∏–Ω–∞–ª—å–Ω–∞—è —Ç–æ—á–Ω–æ—Å—Ç—å: {info['final_accuracy']:.2f}%")
    print(f"üìâ –§–∏–Ω–∞–ª—å–Ω—ã–π loss: {info['final_loss']:.4f}")
    
    # –ó–ê–í–ï–†–®–ï–ù–ò–ï
    print("\n" + "="*70)
    print("‚úÖ –û–ë–£–ß–ï–ù–ò–ï –ó–ê–í–ï–†–®–ï–ù–û!")
    print("="*70)
    print(f"\nüìÅ –ú–æ–¥–µ–ª—å —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∞: {model_path}")
    print(f"üìä –¢–æ—á–Ω–æ—Å—Ç—å: {info['final_accuracy']:.2f}%")
